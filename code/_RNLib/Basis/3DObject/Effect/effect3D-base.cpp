//========================================
// ライフカウント処理
//========================================
float CEffect3D_Base::CountLife(void) {

	if (m_life != NONEDATA) {
		if (--m_life <= 0) {
			Delete();
		}

		return RNLib::Ease().Easing(m_easeType, m_lifeMax - m_life, m_lifeMax);
	}

	return 0.0f;
}