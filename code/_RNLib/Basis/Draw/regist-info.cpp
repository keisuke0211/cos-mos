//========================================
// コンストラクタ
//========================================
CRegistInfoBase::CRegistInfoBase() {

	ClearParameter();
}

//========================================
// デストラクタ
//========================================
CRegistInfoBase::~CRegistInfoBase() {

}

//========================================
// パラメーターのクリア
//========================================
void CRegistInfoBase::ClearParameter(void) {

	m_clippingID = NONEDATA;
}

//========================================
// 描画情報に代入
//========================================
void CRegistInfoBase::AssignToDrawInfo(CDrawInfoBase& drawInfo) {

	drawInfo.m_clippingID = m_clippingID;
}