std::mutex _RNC_Memory::ms_memoryMutex;

//========================================
// [静的]必要分のサイズを確保し、文字列をコピーする
//========================================
void _RNC_Memory::AllocString(char** string, const char* source) {

	// 文字列を解放しておく
	Release(string);

	// コピー元が無い時、終了
	if (source == NULL)
		return;

	// 文字列を新たに生成
	Alloc(string, sizeof(char) * (strlen(source) + 1));

	// 文字列をコピーする
	strcpy(*string, source);
}
